package parasolution.queremosouvirvoce.model;

public class Categorias {

    private int id;
    private String categoria;
    private String pergunta;
    private String cafe;
    private String bebida;
    private String happyhour;
    private String pratos;
    private String doces;
    private String salgados;
    private String metodos;

    public Categorias() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getPergunta() {
        return pergunta;
    }

    public void setPergunta(String pergunta) {
        this.pergunta = pergunta;
    }

    public String getCafe() {
        return cafe;
    }

    public void setCafe(String cafe) {
        this.cafe = cafe;
    }

    public String getBebida() {
        return bebida;
    }

    public void setBebida(String bebida) {
        this.bebida = bebida;
    }

    public String getHappyhour() {
        return happyhour;
    }

    public void setHappyhour(String happyhour) {
        this.happyhour = happyhour;
    }

    public String getPratos() {
        return pratos;
    }

    public void setPratos(String pratos) {
        this.pratos = pratos;
    }

    public String getDoces() {
        return doces;
    }

    public void setDoces(String doces) {
        this.doces = doces;
    }

    public String getSalgados() {
        return salgados;
    }

    public void setSalgados(String salgados) {
        this.salgados = salgados;
    }

    public String getMetodos() {
        return metodos;
    }

    public void setMetodos(String metodos) {
        this.metodos = metodos;
    }
}
